// 64タイプの自己肯定コンテンツを生成
export function generateSelfAffirmationContent(
  fullArchetype: FullArchetype64
): SelfAffirmationContent {
  const [titleStr, baseStr] = fullArchetype.split('_') as [string, BaseArchetype];
  const title = titleStr as TitleType;
  const base = baseStr;
  
  const titleData = TITLE_DATA[title];
  const archetypeData = ARCHETYPE_DATA[base];
  const titleEnhancement = TITLE_ENHANCEMENT[title];
  const abilities = ARCHETYPE_ABILITIES[base];
  
  return {
    uniqueness: `あなたは「${titleData.name}${archetypeData.name}」という、${titleEnhancement.uniqueValue}を持つ特別な存在です。${titleEnhancement.strengthMessage}`,
    
    coreAbilities: abilities.coreAbilities,
    
    activationScenes: abilities.activationScenes,
    
    socialValue: `現代社会では、${archetypeData.group}グループの特性である「${archetypeData.traits.join('・')}」と、${titleData.name}が持つ「${titleData.traits.join('・')}」の組み合わせが特に重要視されています。あなたのような人材は、変化の激しい時代において組織やコミュニティに安定感と成長をもたらす貴重な存在として求められています。`,
    
    growthPotential: [
      `${titleEnhancement.approachStyle}スタイルにより、長期的に大きな成果を生み出す可能性`,
      `${archetypeData.strengths.join('や')}の能力をさらに磨くことで、分野のエキスパートになる可能性`,
      `${titleData.characteristics}という特性を活かし、リーダーシップや専門性を発揮する可能性`
    ],
    
    dailyApplication: {
      work: abilities.workApplications,
      relationships: abilities.relationshipApplications,
      personal: abilities.personalApplications
    },
    
    motivationalMessage: `あなたの「${titleData.name}${archetypeData.name}」としての特性は、世界にとって必要不可欠な価値です。${titleData.approach}あなたらしいペースで、その素晴らしい能力を発揮していってください。あなたが存在することで、周囲の人々はより豊かで意味のある時間を過ごすことができるのです。`
  };
}

// 64タイプすべての自己肯定コンテンツを事前生成（パフォーマンス最適化用）
export const ALL_SELF_AFFIRMATION_CONTENT: Record<FullArchetype64, SelfAffirmationContent> = {} as Record<FullArchetype64, SelfAffirmationContent>;

// 初期化関数
function initializeSelfAffirmationContent() {
  const baseTypes: BaseArchetype[] = [
    'ARC', 'ALC', 'SOV', 'INV',  // 分析家
    'SAG', 'DRM', 'HER', 'BAR',  // 外交官  
    'GUA', 'DEF', 'EXE', 'PRO',  // 番人
    'ART', 'ARS', 'PIO', 'PER'   // 探検家
  ];
  
  const titleTypes: TitleType[] = ['HARMONIC', 'PIONEERING', 'SOLITARY', 'CHALLENGING'];
  
  baseTypes.forEach(base => {
    titleTypes.forEach(title => {
      const fullType = `${title}_${base}` as FullArchetype64;
      ALL_SELF_AFFIRMATION_CONTENT[fullType] = generateSelfAffirmationContent(fullType);
    });
  });
}

// 初期化実行
initializeSelfAffirmationContent();

// 自己肯定コンテンツ取得関数
export function getSelfAffirmationContent(fullArchetype: FullArchetype64): SelfAffirmationContent {
  return ALL_SELF_AFFIRMATION_CONTENT[fullArchetype];
}